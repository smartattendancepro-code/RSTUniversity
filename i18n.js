export function t(key) {
    const lang = localStorage.getItem('sys_lang') || 'en';
    return i18n[lang][key] || key;
}

export function changeLanguage(lang) {
    const dict = i18n[lang];
    if (!dict) return;

    document.documentElement.dir = dict.dir || "rtl";
    document.documentElement.lang = lang;
    document.body.style.fontFamily = dict.font;

    const smartFormat = (key) => {
        if (!key) return "";
        return key
            .replace(/_/g, ' ')
            .replace(/btn|title|label|msg/gi, '')
            .trim()
            .replace(/\b\w/g, l => l.toUpperCase());
    };

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        let newText = dict[key];

        if (!newText && lang === 'en') {
            newText = smartFormat(key);
        }

        if (newText) {
            const icon = el.querySelector('i');
            if (icon) {
                el.innerHTML = `${icon.outerHTML} <span class="btn-text-content">${newText}</span>`;
            } else {
                el.innerText = newText;
            }
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(input => {
        const key = input.getAttribute('data-i18n-placeholder');
        let newText = dict[key];

        if (!newText && lang === 'en') newText = smartFormat(key);

        if (newText) input.placeholder = newText;
    });

    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const key = el.getAttribute('data-i18n-title');
        let newText = dict[key];

        if (!newText && lang === 'en') newText = smartFormat(key);

        if (newText) el.title = newText;
    });

    localStorage.setItem('sys_lang', lang);

    const langBtnText = document.querySelector('.active-lang-text-pro');
    if (langBtnText) langBtnText.innerText = (lang === 'ar') ? 'EN' : 'عربي';

    if (typeof window.resetMainButtonUI === 'function') window.resetMainButtonUI();
}

export function toggleSystemLanguage() {
    const current = localStorage.getItem('sys_lang') || 'en';
    const next = current === 'ar' ? 'en' : 'ar';
    changeLanguage(next);
}

document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('sys_lang') || 'en';
    changeLanguage(saved);
});

window.changeLanguage = changeLanguage;
window.toggleSystemLanguage = toggleSystemLanguage;
window.t = t;
