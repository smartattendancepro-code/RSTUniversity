/**
 * جلب مواد كلية معينة بناءً على الفرقة
 * @param {string} collegeCode - كود الكلية (مثال: "NURS")
 * @param {string|number} level - رقم الفرقة (1, 2, 3, 4, 5)
 * @returns {string[]} قائمة المواد
 */
export function getSubjectsByCollegeAndLevel(collegeCode, level) {
    const levelMap = {
        "1": "first_year",
        "2": "second_year",
        "3": "third_year",
        "4": "fourth_year",
        "5": "fifth_year"
    };

    const collegeData = COLLEGE_SUBJECTS[collegeCode];
    if (!collegeData) return [];

    const yearKey = levelMap[level?.toString()];
    if (!yearKey) return [];

    return collegeData[yearKey] || [];
}

/**
 * جلب قاعات كلية معينة
 * @param {string} collegeCode - كود الكلية
 * @returns {string[]} قائمة القاعات
 */
export function getHallsByCollege(collegeCode) {
    return COLLEGE_HALLS[collegeCode] || [];
}

/**
 * جلب كل مواد كلية معينة (كل الفرق مجمعة)
 * @param {string} collegeCode - كود الكلية
 * @returns {string[]} قائمة كل المواد
 */
export function getAllSubjectsByCollege(collegeCode) {
    const collegeData = COLLEGE_SUBJECTS[collegeCode];
    if (!collegeData) return [];

    return Object.values(collegeData).flat();
}